import numpy as np
import matplotlib.pyplot as plt
import torch

def myimshow(image, ax=plt):
    if type(image)==torch.Tensor:
        image = image.to('cpu').numpy()
    image = np.moveaxis(image, [0, 1, 2], [2, 0, 1])
    image = (image + 1) / 2
    image[image < 0] = 0
    image[image > 1] = 1
    h = ax.imshow(image)
    ax.axis('off')
    return h